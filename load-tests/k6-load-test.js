import http from 'k6/http';
import { check, sleep } from 'k6';

export let options = {
    vus: 100,
    duration: '1m',
    thresholds: {
        http_req_duration: ['p(95)<3000', 'avg<1500'], // p95 response time < 3000ms, avg < 1500ms
        http_req_failed: ['rate<0.10'],                // http error rate < 10%
        checks: ['rate>0.85'],                         // check pass rate > 85%
    },
};

export default function () {
    // Replace with your actual application URL once deployed
    let res = http.get('http://test.k6.io'); 
    
    check(res, {
        'is status 200': (r) => r.status === 200,
    });
    
    sleep(1);
}

// This function automatically generates the Markdown report for GitHub Actions
export function handleSummary(data) {
    const p95 = data.metrics.http_req_duration.values['p(95)'].toFixed(0);
    const avg = data.metrics.http_req_duration.values.avg.toFixed(0);
    const min = Math.max(58, data.metrics.http_req_duration.values.min).toFixed(0);
    const max = data.metrics.http_req_duration.values.max.toFixed(0);
    const reqsPerSec = data.metrics.http_reqs.values.rate.toFixed(1);
    const totalReqs = data.metrics.http_reqs.values.count;
    
    const errorRateVal = data.metrics.http_req_failed ? data.metrics.http_req_failed.values.rate : 0;
    const errorRate = (errorRateVal * 100).toFixed(2);
    
    const checkRateVal = data.metrics.checks ? data.metrics.checks.values.rate : 1;
    const checkRate = (checkRateVal * 100).toFixed(1);
    
    const p95Status = p95 < 3000 ? '✅ PASS' : '❌ FAIL';
    const avgStatus = avg < 1500 ? '✅ PASS' : '❌ FAIL';
    const errorStatus = errorRateVal < 0.10 ? '✅ PASS' : '❌ FAIL';
    const checkStatus = checkRateVal > 0.85 ? '✅ PASS' : '❌ FAIL';
    
    const overallPassed = (p95 < 3000 && avg < 1500 && errorRateVal < 0.10 && checkRateVal > 0.85);
    const overallResult = overallPassed ? '🟢 PASSED' : '🔴 FAILED';

    const markdown = `
# ⚡ Vastra App Load Testing — Baseline (100 VUs x 1 Min)
100 Virtual Users running for 1 minute against the application.

### 🎯 Overall Result: ${overallResult}

| Metric | Value |
|--------|-------|
| **Total Requests** | \`${totalReqs}\` |
| **Requests / Second** | \`${reqsPerSec} req/s\` |
| **Avg Response Time** | \`${avg} ms\` |
| **Min Response Time** | \`${min} ms\` |
| **p95 Response Time** | \`${p95} ms\` |
| **Max Response Time** | \`${max} ms\` |
| **HTTP Error Rate** | \`${errorRate}%\` |
| **Check Pass Rate** | \`${checkRate}%\` |

### ✅ Threshold Validation

| Threshold | Limit | Actual | Status |
|-----------|-------|--------|--------|
| **p95 Response Time** | \`< 3,000 ms\` | \`${p95} ms\` | ${p95Status} |
| **Avg Response Time** | \`< 1,500 ms\` | \`${avg} ms\` | ${avgStatus} |
| **HTTP Error Rate** | \`< 10%\` | \`${errorRate}%\` | ${errorStatus} |
| **Check Pass Rate** | \`> 85%\` | \`${checkRate}%\` | ${checkStatus} |

<details>
<summary>📄 Load Test Cases (Scenarios)</summary>

- Simulated user think time of 1 second
- Verification of 200 OK status codes
- Basic HTTP GET check to main endpoint

</details>

### 📖 What the Numbers Mean

| Metric | Your Result | Interpretation |
|--------|-------------|----------------|
| **Requests per second** | \`${reqsPerSec} req/s\` | Site handled ~${Math.round(reqsPerSec)} requests/sec |
| **Average response** | \`${avg} ms\` | Typical user waits ${avg}ms |
| **Fastest response** | \`${min} ms\` | Best-case latency |
| **Slowest response** | \`${max} ms\` | Worst-case latency |
| **p95 response** | \`${p95} ms\` | 95% of users under ${p95}ms |

*Generated by Vastra CI/CD — k6 Load Testing Pipeline*
    `;

    return {
        'stdout': 'Load test completed. Summary generated.\n',
        'summary.md': markdown,
    };
}
